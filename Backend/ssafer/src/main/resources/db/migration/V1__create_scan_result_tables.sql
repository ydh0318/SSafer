CREATE TABLE scans (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    project_id BIGINT NOT NULL,
    requested_by_user_id BIGINT,
    request_actor_type VARCHAR(20) NOT NULL DEFAULT 'USER',
    agent_id BIGINT,
    custom_rule_set_id BIGINT,
    parent_scan_id BIGINT,
    scan_mode VARCHAR(20) NOT NULL,
    status VARCHAR(20) NOT NULL DEFAULT 'REQUESTED',
    progress_step VARCHAR(100),
    failure_reason TEXT,
    target_snapshot_json JSONB,
    raw_result_json JSONB,
    raw_result_path VARCHAR(500),
    requested_at TIMESTAMP NOT NULL,
    started_at TIMESTAMP,
    completed_at TIMESTAMP,
    last_updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_scans_request_actor_type
        CHECK (request_actor_type IN ('USER', 'GUEST', 'SYSTEM')),
    CONSTRAINT chk_scans_scan_mode
        CHECK (scan_mode IN ('UPLOAD', 'AGENT')),
    CONSTRAINT chk_scans_status
        CHECK (status IN ('REQUESTED', 'QUEUED', 'RUNNING', 'DONE', 'FAILED', 'CANCELED'))
);

CREATE TABLE scan_nodes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    scan_id BIGINT NOT NULL,
    node_key VARCHAR(255) NOT NULL,
    node_name VARCHAR(255),
    node_type VARCHAR(100),
    metadata_json JSONB,
    created_at TIMESTAMP NOT NULL
);

CREATE TABLE scan_findings (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    scan_id BIGINT NOT NULL,
    scan_nodes_id BIGINT NOT NULL,
    source_type VARCHAR(20) NOT NULL DEFAULT 'TRIVY',
    fingerprint VARCHAR(255) NOT NULL,
    severity VARCHAR(20) NOT NULL,
    category VARCHAR(100) NOT NULL,
    title VARCHAR(255) NOT NULL,
    description TEXT,
    file_path VARCHAR(500),
    resource_name VARCHAR(255),
    rule_code VARCHAR(100),
    attack_scenario TEXT,
    remediation_guide TEXT,
    raw_snippet_json JSONB,
    resolution_status VARCHAR(20) NOT NULL DEFAULT 'OPEN',
    patch_approved_by_user_id BIGINT,
    patch_approved_at TIMESTAMP,
    patch_result_message TEXT,
    backup_file_name VARCHAR(255),
    backup_file_path VARCHAR(500),
    backup_metadata_json JSONB,
    patched_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL,
    CONSTRAINT chk_scan_findings_source_type
        CHECK (source_type IN ('TRIVY', 'CUSTOM_RULE', 'AI')),
    CONSTRAINT chk_scan_findings_severity
        CHECK (severity IN ('CRITICAL', 'HIGH', 'MEDIUM', 'LOW', 'INFO')),
    CONSTRAINT chk_scan_findings_resolution_status
        CHECK (resolution_status IN ('OPEN', 'IN_PROGRESS', 'RESOLVED', 'IGNORED'))
);

ALTER TABLE scan_nodes
    ADD CONSTRAINT fk_scan_nodes_scan
        FOREIGN KEY (scan_id) REFERENCES scans (id) ON DELETE CASCADE;

ALTER TABLE scan_findings
    ADD CONSTRAINT fk_scan_findings_scan
        FOREIGN KEY (scan_id) REFERENCES scans (id) ON DELETE CASCADE;

ALTER TABLE scan_findings
    ADD CONSTRAINT fk_scan_findings_scan_node
        FOREIGN KEY (scan_nodes_id) REFERENCES scan_nodes (id) ON DELETE CASCADE;

CREATE INDEX idx_scans_project_id
    ON scans (project_id);

CREATE INDEX idx_scans_status
    ON scans (status);

CREATE UNIQUE INDEX uk_scan_nodes_scan_id_node_key
    ON scan_nodes (scan_id, node_key);

CREATE INDEX idx_scan_findings_scan_id
    ON scan_findings (scan_id);

CREATE INDEX idx_scan_findings_scan_id_node_id
    ON scan_findings (scan_id, scan_nodes_id);

CREATE INDEX idx_scan_findings_scan_id_severity
    ON scan_findings (scan_id, severity);

CREATE UNIQUE INDEX uk_scan_findings_scan_id_node_id_fingerprint
    ON scan_findings (scan_id, scan_nodes_id, fingerprint);
